package com.demo.demo.ci;

public class Certi {
    private String name;

    public Certi(String name) {
        this.name = name;
    }

    public Certi() {
        super();
    }

    @Override
    public String toString() {
        return "Certi{" +
                "name='" + this.name + '\'' +
                '}';
    }
}
